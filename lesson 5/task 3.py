# Открываем файл для чтения с указанием кодировки
with open('Text.txt', 'r', encoding='utf-8') as file:
    # Инициализируем список для хранения фамилий сотрудников с окладом менее 20 тысяч
    less_than_20k = []

    # Инициализируем переменную для подсчета суммы окладов сотрудников
    total_income = 0

    # Инициализируем переменную для подсчета количества сотрудников
    count = 0

    # Читаем каждую строку файла
    for line in file:
        # Разделяем строку на фамилию и оклад
        employee, income = line.split()
        income = float(income)

        # Добавляем оклад к общей сумме
        total_income += income

        # Увеличиваем счетчик сотрудников
        count += 1

        # Проверяем, является ли оклад меньше 20 тысяч
        if income < 20000:
            # Добавляем фамилию в список
            less_than_20k.append(employee)

    # Выводим фамилии сотрудников с окладом менее 20 тысяч
    print("Фамилии сотрудников с окладом менее 20 тысяч:")
    for employee in less_than_20k:
        print(employee)

    # Вычисляем среднюю величину дохода сотрудников
    average_income = total_income / count
    print(f"Средняя величина дохода сотрудников: {average_income:.2f}")
