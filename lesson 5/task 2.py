# Открываем файл для чтения
with open('data.txt', 'r') as file:
    # Считываем все строки из файла
    lines = file.readlines()

    # Подсчитываем общее количество строк в файле
    total_lines = len(lines)

    # Перебираем строки и подсчитываем количество слов в каждой строке
    for i, line in enumerate(lines, 1):
        # Удаляем символы перевода строки из строки
        line = line.strip()

        # Разделяем строку на слова
        words = line.split()

        # Подсчитываем количество слов в текущей строке
        word_count = len(words)

        # Выводим информацию о строке
        print(f"Строка {i}: Количество слов - {word_count}")

# Выводим общее количество строк
print(f"Общее количество строк: {total_lines}")
